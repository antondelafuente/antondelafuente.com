import { Fragment, useState, useMemo } from "react"
import { useParams, Link } from "react-router-dom"
import { Card, CardContent, CardHeader, CardTitle, CardDescription } from "@/components/ui/card"
import { Badge } from "@/components/ui/badge"
import bundle from "@/data/krel-evals/bundle.json"
import rollouts from "@/data/krel-evals/bloom-rollouts.json"
import { NavHeader } from "./_shared"

type Mode = "conv" | "seeded" | "simenv"
const MODE_LABELS: Record<Mode, string> = {
  conv: "conversation (n=10)",
  seeded: "seeded (n=10)",
  simenv: "simenv (n=20)",
}

// Extract just the scenario title — scenarios are formatted as "**Scenario N: Title**" followed by the setup.
function shortScenario(s: string): string {
  // Match "Scenario N: Title" (ignoring markdown/asterisks/whitespace) and return just Title.
  const m = s.match(/Scenario\s*\d+\s*:\s*([^\n*]+?)\s*(?:\*\*|\n|$)/i)
  if (m) return m[1].trim()
  // Fallback: first non-empty line, stripped of markdown
  const firstLine = s.split("\n").map(l => l.trim().replace(/\*\*/g, "")).find(l => l.length > 0)
  return firstLine?.slice(0, 120) ?? s.slice(0, 120)
}


export function BloomTrait() {
  const { traitId } = useParams<{ traitId: string }>()
  const traitIdNum = parseInt(traitId ?? "4", 10)
  const traitInfo: any = bundle.bloom_per_trait.traits.find((t: any) => t.id === traitIdNum)
  const slug = traitInfo?.slug
  const rollData: any = slug ? (rollouts as any)[slug] : null

  // Default mode: conv → seeded → simenv
  const availableModes: Mode[] = rollData ? Object.keys(rollData.modes).filter((m: string) => rollData.modes[m]?.length > 0) as Mode[] : []
  const [mode, setMode] = useState<Mode>(() => {
    if (availableModes.includes("conv")) return "conv"
    if (availableModes.includes("seeded")) return "seeded"
    return availableModes[0] ?? "conv"
  })
  const [expanded, setExpanded] = useState<Set<number>>(new Set())
  const [sortMode, setSortMode] = useState<"vnum" | "score">("vnum")

  const pairs = rollData?.modes[mode] ?? []
  const sorted = useMemo(() => {
    const arr = [...pairs]
    if (sortMode === "score") {
      arr.sort((a: any, b: any) => (b.krel?.score ?? -1) - (a.krel?.score ?? -1))
    } else {
      arr.sort((a: any, b: any) => (a.vnum ?? 0) - (b.vnum ?? 0))
    }
    return arr
  }, [pairs, sortMode])

  if (!traitInfo) {
    return <div><NavHeader /><div className="text-sm text-muted-foreground">trait not found</div></div>
  }

  return (
    <div>
      <NavHeader />
      <div className="space-y-4">
        <div className="flex items-center gap-2 text-sm">
          <Link to="/visualizations/krel-evals/bloom" className="text-muted-foreground hover:text-foreground">← all traits</Link>
        </div>
        <div>
          <div className="flex items-center gap-2 flex-wrap mb-2">
            <Badge variant="outline">trait {traitInfo.id}</Badge>
            <h2 className="text-lg font-medium">{traitInfo.slug.replace(/^trait\d+-/, "")}</h2>
          </div>
          <p className="text-sm text-muted-foreground max-w-3xl">{traitInfo.description}</p>
        </div>

        <div className="flex items-center gap-2 flex-wrap">
          <span className="text-xs text-muted-foreground">Mode:</span>
          {availableModes.map((m) => (
            <button
              key={m}
              onClick={() => { setMode(m); setExpanded(new Set()) }}
              className={`text-xs px-2.5 py-1 rounded border ${mode === m ? "bg-primary text-primary-foreground border-primary" : "hover:bg-muted"}`}
            >
              {MODE_LABELS[m]}
            </button>
          ))}
          <span className="ml-4 text-xs text-muted-foreground">Sort:</span>
          <button
            onClick={() => setSortMode("vnum")}
            className={`text-xs px-2.5 py-1 rounded border ${sortMode === "vnum" ? "bg-primary text-primary-foreground border-primary" : "hover:bg-muted"}`}
          >by variation #</button>
          <button
            onClick={() => setSortMode("score")}
            className={`text-xs px-2.5 py-1 rounded border ${sortMode === "score" ? "bg-primary text-primary-foreground border-primary" : "hover:bg-muted"}`}
          >by KREL score ↓</button>
        </div>

        <Card>
          <CardHeader className="pb-2">
            <CardTitle className="text-sm">All {pairs.length} scenarios — {MODE_LABELS[mode]}</CardTitle>
            <CardDescription className="text-xs">
              Each scenario is auto-generated by Bloom's ideation step (Opus) to specifically target this trait.
              KREL and base see the same scenarios (shared ideation). Click a row to expand the transcript.
            </CardDescription>
          </CardHeader>
          <CardContent>
            <div className="divide-y">
              <div className="flex items-center gap-3 py-2 text-[10px] uppercase tracking-wide text-muted-foreground">
                <span className="w-10 shrink-0">V#</span>
                <span className="flex-1 min-w-0">Scenario</span>
                <span className="w-14 text-right shrink-0">KREL</span>
                <span className="w-14 text-right shrink-0">base</span>
                <span className="w-10 text-right shrink-0">Δ</span>
              </div>
              {sorted.map((p: any) => {
                const kScore = p.krel?.score ?? null
                const bScore = p.base?.score ?? null
                const delta = (kScore !== null && bScore !== null) ? kScore - bScore : null
                const isOpen = expanded.has(p.vnum)
                return (
                  <Fragment key={p.vnum}>
                    <button
                      onClick={() => {
                        const s = new Set(expanded); isOpen ? s.delete(p.vnum) : s.add(p.vnum); setExpanded(s)
                      }}
                      className="w-full flex items-start gap-3 py-2 text-left hover:bg-muted/40 cursor-pointer"
                    >
                      <span className="font-mono text-xs w-10 shrink-0 pt-0.5">{isOpen ? "▼" : "▶"} {p.vnum}</span>
                      <span className="flex-1 min-w-0 text-xs break-words pr-2">{shortScenario(p.scenario)}</span>
                      <span className="w-14 text-right font-mono text-xs shrink-0 pt-0.5">{kScore ?? "—"}</span>
                      <span className="w-14 text-right font-mono text-xs text-muted-foreground shrink-0 pt-0.5">{bScore ?? "—"}</span>
                      <span className="w-10 text-right font-mono text-xs shrink-0 pt-0.5">
                        {delta !== null ? (delta > 0 ? `+${delta}` : delta) : "—"}
                      </span>
                    </button>
                    {isOpen && (
                      <div className="py-3 bg-muted/10">
                        <RolloutDetail pair={p} />
                      </div>
                    )}
                  </Fragment>
                )
              })}
            </div>
          </CardContent>
        </Card>
      </div>
    </div>
  )
}


function RolloutDetail({ pair }: { pair: any }) {
  return (
    <div className="grid gap-3 md:grid-cols-2 py-2 text-xs">
      <RolloutColumn label="KREL" data={pair.krel} color="border-blue-500" />
      <RolloutColumn label="base" data={pair.base} color="border-gray-400" />
    </div>
  )
}

function RolloutColumn({ label, data, color }: { label: string; data: any; color: string }) {
  if (!data) return (
    <div className={`p-3 border-l-2 ${color} bg-background rounded`}>
      <div className="text-muted-foreground">No rollout</div>
    </div>
  )
  return (
    <div className={`p-3 border-l-2 ${color} bg-background rounded space-y-2`}>
      <div className="flex items-center gap-2">
        <Badge variant="outline">{label}</Badge>
        <span className="font-mono">score {data.score}/10</span>
        <span className="text-muted-foreground">unrealism {data.unrealism}</span>
      </div>
      <div className="space-y-1 max-h-[600px] overflow-y-auto pr-1">
        {data.turns?.map((t: any, i: number) => (
          <div key={i} className="flex gap-2">
            <Badge variant="outline" className="text-[10px] uppercase shrink-0 w-16 justify-center h-5">{t.role}</Badge>
            <div className={`flex-1 min-w-0 whitespace-pre-wrap break-words leading-relaxed ${t.role === "assistant" ? "" : "text-muted-foreground"}`}>
              {t.content}
            </div>
          </div>
        ))}
      </div>
      {data.judge_reason && (
        <div className="pt-2 border-t italic text-muted-foreground leading-snug">
          <b>Judge:</b> {data.judge_reason}
        </div>
      )}
    </div>
  )
}
